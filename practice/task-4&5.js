// Local storage
// 1) The stored data is not deleted when the browser is closed.
// 2) The data is stored on the browser and system.
// 3) It allows 10MB of data to be stored.
// 4) Local storage is useful for storing data that the user will need to access later, such as offline data.

// Session storage
// 1) The data is stored only for the session and will be deleted when the browser is closed.
// 2) The data is stored on the browser only.
// 3) It allows 5MB of data to be stored.
// 4) Session storage is a great way to improve the performance of your web applications.

// Internet Cookies
// 1) The data can be set to expire at a certain time.
// 2) The data is stored on the browser only.
// 3) The storage capacity is limited to 4KB of data.
// 4) Cookies are a good choice for storing data that should not be persisted for a long time, such as session IDs.